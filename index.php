                <h1> Incluindo Pessoas Com Deficiência </h1>

                <p> Para garantir a inclusão das pessoas com deficiência na internet, existe um padrão global de acessibilidade chamado WCAG, que é a sigla em inglês para Diretrizes de Acessibilidade de Conteúdo da Web.Este é um guia que todas as páginas devem seguir para garantir a acessibilidade para o maior número de pessoas possível. Porém, assim como tudo relacionado a tecnologia, ele também passa por mudanças, buscando acompanhar a metamorfose da internet, canais de comunicação e conteúdos.Por isso, mesmo depois de pronto, é importante que as pessoas responsáveis pelo desenvolvimento de websites estejam atentas às possíveis atualizações das diretrizes. </p>

                <p> Como tornar seu site acessível para as pessoas com deficiência? </p>

                <ul>
                    <li> Textos alternativos (ALT): Incluir ALTs, que são as descrições de imagens, nas fotos e ilustrações da página, permite que as pessoas cegas ou com deficiência visual consumam aquele conteúdo de forma plena. Elas normalmente navegam utilizando leitores de tela, que transformam as imagens em áudios, desde que contenham a descrição por escrito dos elementos visuais presentes. </li>
                    <li> Plugin de acessibilidade em Libras (Língua Brasileira de Sinais): Instalar plugin de acessibilidade em Libras é uma boa dica para a inclusão das pessoas surdas ou com deficiência auditiva que fazem o uso dessa língua! O Hand Talk Plugin, por exemplo, é uma solução que, com a ajuda de simpáticos tradutores virtuais, traduz todo o texto do site em Português, para a Língua Brasileira de Sinais. </li>
                    <li> Legendas e Janela de Libras: Incluir legendas e janela de Libras garante que o conteúdos audiovisuais estejam acessíveis para as pessoas surdas e com deficiência auditiva. Essa ferramenta ainda beneficia outras pessoas, que podem consumir os vídeos sem ligar o áudio. </li>
                    <li> Fontes Com Boa Legibilidade: Escolher fontes de fácil leitura impacta no conforto de todos os usuários, mas principalmente daqueles com deficiências visuais ou cognitivas. </li>
                </ul>

